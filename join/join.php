        <h2>Syarat Pendirian Koperasi</h2>
        <p>Pasal 12 Permen Koperasi dan UKM No. 9/2018 tentang Penyelenggaraan dan Pembinaan Perkoperasian telah mengatur mengenai persyaratan pendirian Koperasi di Indonesia. Pendirian koperasi dilakukan dengan mengadakan rapat pendirian koperasi yang harus dihadiri oleh para pendiri, dan juga dihadiri oleh pejabat yang berguna untuk melakukan penyuluhan terkait koperasi.</p>
        <p>Jumlah orang yang hadir dalam pendirian koperasi akan berbeda tergantung jenisnya. Untuk pendirian koperasi primer dihadiri oleh 20 orang dan akan lebih sedikit untuk koperasi sekunder.</p>

        <h5>Syarat Koperasi Primer</h5>
        <p>Pendiri koperasi mengajukan akta pendirian koperasi baik itu secara tertulis maupun secara elektronik kepada Menteri dengan melampirkan beberapa persyaratan, yaitu:</p>

        <ul>
            <li>Dua rangkap akta pendirian koperasi (bermaterai)</li>
            <li>Berita acara rapat pendirian koperasi</li>
            <li>Surat bukti penyetoran modal</li>
            <li>Rencana awal kegiatan koperasi</li>
        </ul>

        <h5>Syarat Koperasi Sekunder</h5>

        <p>Syarat untuk mendirikan koperasi sekunder sama seperti koperasi primer, namun terdapat beberapa tambahan dokumen berupa:</p>

        <ul>
            <li>Hasil berita acara rapat pendirian </li>
            <li>Keputusan pengesahan badan hukum koperasi primer dan/atau sekunder</li>
            <li>NPWP aktif untuk setiap calon anggota koperasi primer dan/atau sekunder</li>
        </ul>

        <h5>Syarat Koperasi Simpan Pinjam (KSP) </h5>

        <p>Terdapat persyaratan khusus untuk KSP yang bisa dilihat pada:</p>

        <ul>
            <li>Pasal 10 ayat (5) Permen Koperasi dan UKM No. 9 thn 2018 </li>
        </ul>

        <p>Setelah pengajuan akta pendirian koperasi, dan mendapatkan penilaian terkait anggaran dasar serta persyaratan administrasi. Maka Menteri akan menerbitkan dua opsi surat, yaitu Surat Keputusan (SK) penerimaan, atau penolakan.</p>

        <h5>Tahapan dan Prosedur Pendirian Koperasi</h5>

        <p>Peraturan Menteri Koperasi dan UKM Nomor 9 Tahun 2018 telah mengatur tentang tahapan dan tata cara pendirian koperasi, yaitu :</p>

        <ul>
            <li>Perencanaan Pendirian Koperasi</li>
            <li>Penyampaian rencana dan konsultasi ke daerah pusat maupun dinas koperasi dan umkm</li>
            <li>Rapat Pendirian Koperasi</li>
            <li>Verifikasi Nama Koperasi</li>
            <li>Pengajuan Pengesahan Akta Pendirian Koperasi</li>
            <li>Verifikasi Dokumen Permohonan</li>
            <li>Mekanisme di Sisminbhkop</li>
            <li>Pengesahan Pendirian Koperasi</li>
        </ul>

